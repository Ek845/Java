// Вывести все простые числа от 1 до 1000


public class Seminar_1_2 {
    public static void main(String[] args) {
        for (int i = 3; i < 1000; i += 2) {
            for (int j = 2; j < 1000; j++) {
                if (i % j != 0);
                    System.out.println(i);
        }
    }
}

}
